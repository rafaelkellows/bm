            <h3>Gestão de Risco</h3>
            <p><strong>Segurança e liquidez para nossos investimentos</strong></p>
            <p>Com uma história consolidada no sistema financeiro, o Banco Máxima segue todas as regras definidas pelo Banco Central e conta com ferramentas aprimoradas de avaliação e gestão de risco.</p>
            <p>Controlamos a exposição de nossos ativos para garantir uma administração de recursos eficiente, que considere diferentes tipos de ocorrência em nossas operações. O objetivo é calcular riscos inerentes às nossas linhas de negócio e eliminar, com o auxílio da área de gestão, quaisquer ameaças que não contribuam para os retornos financeiros.</p>
            <p>Entre nossos procedimentos de análise, está a avaliação diária da liquidez de nossos investimentos e um rígido controle de eventuais desequilíbrios entre ativos negociados e passivos exigíveis, ou seja, descasamento entre pagamentos e recebimentos que possam afetar a qualidade do nosso portfólio.</p>
            <p>Como primamos pela transparência, periodicamente divulgamos informações sobre nossa carteira de investimentos de forma a esclarecer nossa atuação.</p>
