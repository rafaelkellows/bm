            <h3>Quem Somos</h3>
            <p>Somos um grupo financeiro com mais de 30 anos de experiência no país. Desde o início de nossa atuação, temos assumido uma posição de pioneirismo no lançamento de produtos e na antecipação de tendências, com marcantes participações institucionais nos segmentos em que operamos.</p>
            <p>Nosso objetivo é apresentar as melhores soluções de investimento, tendências e oportunidades que surgem com as constantes transformações e o amadurecimento do mercado brasileiro.</p>
            <p class="img _f-left _v-middle _t-normal">
              <span><img src="images/pages/ilustra-quem-somos.png" alt="" /></span>
              <em>A partir de 2010, direcionamos nosso foco para a concessão de crédito imobiliário, especialmente para empresas do setor de construção civil, com uma linha de atuação desenhada para propiciar aos clientes sempre os melhores negócios.</em>
            </p>
            <p>Trabalhamos com a estruturação de produtos personalizados, como capital de giro e compra de carteiras de construtoras, de acordo com cada tipo de necessidade. Na área de investimentos, temos alternativas que oferecem segurança, ótima rentabilidade e isenção fiscal, como fundos de investimento imobiliário, LCI (letra de crédito imobiliário), e produtos customizados, como o CRI (Certificado de Recebível Imobiliário).</p>
            <p>Em 2011, incluímos a área de câmbio entre as nossas principais linhas de negócio. Oferecemos soluções para empresas exportadoras e importadoras, e atuamos também com o câmbio pronto para pessoa física e jurídica. Por meio da Máxima CCTVM, trabalhamos ainda com a negociação de dólar turismo, além de outras moedas.</p>
          </div>
          <div class="cnt-historico">
            <h3>Histórico</h3>
            <p>O Banco Máxima é a evolução de um grupo financeiro que tem em seu DNA a capacidade de liderar mudanças. Na década de 80, iniciamos nosso trabalho como consultoria e assessoramos o governo brasileiro no Programa Nacional de Desestatização (PND), que resultou no fortalecimento de grandes empresas, como Vale e CSN.</p>
            <p>Também tivemos papel primordial na organização do mercado de balcão, estabelecendo os pilares para a constituição da SOMA (Sociedade Operadora do Mercado de Acesso).</p>
            <p>Ao longo da nossa existência, fomos pioneiros em diversos produtos. Como corretora e financeira, atuamos fortemente no mercado de capitais e criamos o primeiro fundo de direitos creditórios do país.</p>
            <p>O ano de 2010 marcou a expansão das atividades do Banco Máxima e de suas empresas para o seguimento de mercado imobiliário, que tem sido abordado pela aquisição e securitização de créditos imobiliários.          </p>
