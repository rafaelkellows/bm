            <p><h4>Passo a passo para o empréstimo</h4></p>

            <p>Tomar um Empréstimo com Garantia de Imóvel é muito simples. O primeiro passo é simular o valor das parcelas, de forma que você possa calcular como seria seu planejamento financeiro. Nesta etapa, o contato com o banco poderá lhe ajudar a definir o melhor plano “versus” sua atual necessidade.</p>
            
            <p>Veja o passo a passo:</p>
            
            <ul class="list _decimal">
              <li><strong>Simulação</strong><br>
              Você faz uma simulação em nosso site e se estiver de acordo com o que você precisa preencha os campos finais e clique em “Solicitar Contato”, em breve será contatado pelos nossos consultores.</li>
              <br>
              <li><strong>Entrevista com o cliente e envio da documentação</strong><br>
              Nesta etapa, tiramos todas as suas dúvidas e explicamos detalhes da operação. Aceitando as condições enviaremos um check list da documentação necessária para iniciarmos a análise</li>
              <br>
              <li><strong>Análise de Crédito</strong><br>
              Após o envio da documentação solicitada será feita uma análise do perfil, da renda e comprometimento atual do cliente. Em até 48 horas daremos a resposta do crédito Pré-Aprovado com validade de 10 dias.<br>
              Estando de acordo precisamos receber outros comprovantes (simples) para aprovarmos o Crédito e seguir com as outras fases</li>
              <br>
              <li><strong>Avaliação do Imóvel</strong><br>
              Nesta fase, haverá avaliação do imóvel por uma empresa contratada e indicada pelo Banco Máxima, para a constatação se objeto da operação está de acordo com os tipos de imóveis aceitos pelo banco (laudo de avaliação)</li>
              <br>
              <li><strong>Análise Jurídica</strong><br>
              Estando a renda e o imóvel aprovados nas Políticas de Crédito internas do banco, faremos análise dos documentos restantes do check-list, fase de análise jurídica.<br>
              Vale salientar que as fases do processo são muito rápidas e ocorrem, praticamente, simultaneamente ou logo em seguida da anterior.</li>
              <br>
              <li><strong>Formalização</strong><br>
              A área responsável recebe as condições aprovadas e monta uma pasta com os documentos. Verifica se esta tudo certo e completo, inclusive com as aprovações anteriores de crédito e do imóvel, e emite o contrato para assinaturas</li>
              <br>
              <li><strong>Liberação do recurso</strong><br>
              De posse do contrato este deverá ser enviado ao Cartório de Imóveis da região do imóvel para o registro da alienação fiduciária. Após entrega do registro ao banco, em até 5 dias úteis, o recurso será liberado na conta corrente informada no início da operação</li>
            </ul>
